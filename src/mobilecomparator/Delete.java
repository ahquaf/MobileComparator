    private void addItems() {
        
        jComboBox1.removeAllItems();
        
        try{
            
            Class.forName("com.mysql.jdbc.Driver");
            
            Connection  con = DriverManager.getConnection( "jdbc:mysql://localhost:3306/project","root","ahquaf");
            PreparedStatement stmt = con.prepareStatement("select distinct(mobile_name) from mobile_info");
            
            ResultSet rs = stmt.executeQuery();
            
            while(rs.next()){
                jComboBox1.addItem(rs.getString("mobile_name"));
            }
            
            con.close();
            
        }catch(Exception e){
            System.out.println(e);
        }
    }
